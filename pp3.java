
class Type_Casting { 
	public static void main(String[] args) 
	{ 
		int num1 = 10; 

		// from int to long 
		long num2 = num1; 

		// from int to double 
		double num3 = num1; 

		System.out.println("Integer: " + num1); 
		System.out.println("Long: " + num2); 
		System.out.println("Double: " + num3); 
	} 
}
